public class POJO {
        int id;
        int user_id;
        int count;
        String country;

    public POJO(int i)
            {
                id = i;
            }
    public POJO() {}
    
    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }
}
